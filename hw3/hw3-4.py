def my_func1(x, y):
    return 1 / (x ** abs(y))

def my_func2(x, y):
    for i in range(abs(y) - 1):
        x = x * x
    return 1 / x

x = int(input("Введите действительное положительное число: "))
y = int(input("Введите целое отрицательное число: "))

print(f"Возведение в степень с помощью оператора **: {my_func1(x, y)}")
print(f"Возведение в степень с использованием цикла: {my_func2(x, y)}")
