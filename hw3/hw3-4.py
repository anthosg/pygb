message = "4. Программа принимает действительное положительное число x\n" \
        "и целое отрицательное число y.\n" \
        "Необходимо выполнить возведение числа x в степень y.\n" \
        "Задание необходимо реализовать в виде функции my_func(x, y).\n" \
        "При решении задания необходимо обойтись без встроенной функции\n" \
        "возведения числа в степень.\n\n" \
        "** Подсказка:** попробуйте решить задачу двумя способами.\n" \
        "Первый — возведение в степень с помощью оператора **.\n" \
        "Второй — более сложная реализация без оператора **, предусматривающая использование цикла.\n"
